using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace _13253039HW._1
{
    class MathOperation
    {
        public double Toplama(double number1, double number2)
        {
            return number1 + number2;
        }

        public double Cikarma(double number1, double number2)
        {
            return number1 - number2;
        }

        public double Carpma(double number1, double number2)
        {
            return number1 * number2;
        }

        public double Bolme(double number1, double number2)
        {
            return number1 / number2;
        }
        

    }
}
